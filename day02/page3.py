# type casting
# - changing the data type of the variable

# convert any type to string
# call a function: str()
num1 = str(num)
print("num1 = ", num1)
print("type of num1 = ", type(num1))

can_vote = True
print("type of can_vote = ", type(can_vote))

str_can_vote = str(can_vote)
print("type of str can_vote = ", type(str_can_vote))

print("True in str = ", str(True))
